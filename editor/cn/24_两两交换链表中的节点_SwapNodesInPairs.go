type ListNode struct {
	Val  int
	Next *ListNode
}

func swapPairs1(head *ListNode) *ListNode {

	dummyHead := &ListNode{0, head}
	tmp := dummyHead
	for tmp.Next != nil && tmp.Next.Next != nil {
		node1 := tmp.Next
		node2 := tmp.Next.Next

		tmp.Next = node2
		node1.Next = node2.Next
		node2.Next = node1
		tmp = node1
	}

	return dummyHead.Next
}
