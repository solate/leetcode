/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func swapPairs(head *ListNode) *ListNode {

	if head == nil || head.Next == nil {
		return head
	}

	newHead := head.Next                // 2  4
	head.Next = swapPairs(newHead.Next) // 3.next = 4  4.next = 3  1->2->3<->4
	newHead.Next = head
	return newHead
}

